var estilo = document.getElementById('Style')

function changeHREF(){
    if(estilo.href == "https://code.getmdl.io/1.3.0/material.deep_orange-orange.min.css"){
        estilo.href = "https://code.getmdl.io/1.3.0/material.blue_grey-orange.min.css"
    } else{
        estilo.href = "https://code.getmdl.io/1.3.0/material.deep_orange-orange.min.css"
    }
}